class Solution
{
    public:
    //Function to sort a linked list of 0s, 1s and 2s.
    Node* segregate(Node *head) {
        int cnt_0 = 0, cnt_1=0, cnt_2 =0;
        Node *temp=head;
        while(temp){
            if(temp->data == 0)
                cnt_0 ++;
            else if(temp->data == 1)
                cnt_1 ++;
            else cnt_2 ++;
            
            temp = temp->next;
        }
        Node *p=new Node(-1);
        Node *dummy = p;
        while(cnt_0 --){
            dummy->next = new Node(0);
            dummy = dummy->next;
        }
        
        while(cnt_1--){
            dummy->next= new Node(1);
            dummy=dummy->next;
        }
        
        while(cnt_2--){
            dummy->next=new Node(2);
            dummy=dummy->next;
        }
        dummy->next=NULL;
        return p->next;
        
        // Add code here
        
    }
};


